package r_20240708;

public class Ex07 {
	public static void main(String[] args) {
		// 증가감 연산자
		int result, i ;
		i = 1;
		result = i + 1;
		System.out.println(result);
		result = result + 1;
		System.out.println(result);
		result = result + 1;
		System.out.println(result);
		result++; // result = result + 1;
		System.out.println(result);
		result++;// result = result + 1;
		System.out.println(result);
		++result;// result = result + 1;
		System.out.println(result);
		result = result - 1;
		System.out.println(result);
		result--;
		System.out.println(result);
		--result;
		System.out.println(result);
	}
}
