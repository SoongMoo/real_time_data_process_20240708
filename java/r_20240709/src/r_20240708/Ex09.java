package r_20240708;

public class Ex09 {
	public static void main(String[] args) {
		// 대입연산자. =
		int i = 10;
		i = 20;
		System.out.println(i);
		i = i + 1;
		i++;
		++i;
		i += 1; // 대입연산자
		System.out.println(i);
		i = i + 2;
		i += 2;
		i = i + 3;
		i += 3;
		i = i - 1;
		--i;
		i--;
		i -= 1;
		i = i -3;
		i -= 3;
		i *= 2; // i = i * 2;
		// i = i / 4;
		i /= 4;
		// i = i % 3
		i %= 3;
		/*
		 * += , -=, *= , /= , %=  
		 */
	}
}
