package r_20240708;

public class Ex10 {
	public static void main(String[] args) {
		// 비교연산자 : boolean
		int i=10, j=20;
		boolean result;
		result = i > j;
		System.out.println(result);
		result = i < j;
		System.out.println(result);
		i = 10; j = 10;
		result = i >= j;
		System.out.println(result);
		result = i <= j;
		System.out.println(result);
		result = i == j;
		System.out.println(result);
		j = 20;
		result =  i != j;
		System.out.println(result);
		j = 10;
		result = i != j;
		System.out.println(result);
		// < , >, <= , >= , == , !=
		
	}

}
