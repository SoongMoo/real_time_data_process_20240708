package r_20240711;

public class Ex05 {

	public static void main(String[] args) {
		int i [] = new int[4];
		for(int z : i) {
			System.out.println(z);
		}
		double d[] = new double[4];
		for(double z : d) {
			System.out.println(z);
		}
		boolean bl [] = new boolean[4];
		for(boolean z : bl) {
			System.out.println(z);
		}
		String str [] = new String[3];
		for(String z : str) {
			System.out.println(z);
		}
		char ch [] = new char[3];
		for(char z : ch) {
			System.out.println();
		}
		System.out.println(""); // blank
	}

}
