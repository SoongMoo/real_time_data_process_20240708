package j20240827;

public class Ex03 {
	public static void main(String[] args) {
		// 비교연산자 
		// <, > ,<=,>= , == , !=
		// 비교연산자의 결과값은 부울타입이 온다.
		int i = 10;
		int j = 20;
		i = j; // 대입
	    System.out.println(i > j); // 10 > 20 : false
	    System.out.println(i == j); // 10 == 20 : false
	    System.out.println(i != j); // true
	}
}